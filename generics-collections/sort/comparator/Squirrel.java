public class Squirrel {
	private String species;
	private int weight;

	public Squirrel(String species, int weight) {
		this.species = species;
		this.weight = weight;
	}

	public String toString() {
		return species;
	}

	public String getSpecies() {
		return species;
	}

	public int getWeight() {
		return weight;
	}
}
